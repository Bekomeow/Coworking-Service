package org.beko.model.types;

public enum ActionType {
    /**
     * Represents the action of user registration.
     */
    REGISTRATION,

    /**
     * Represents the action of user authorization.
     */
    AUTHORIZATION,

    /**
     * Represents the action of booking place.
     */
    BOOK_PLACE,

    /**
     * Represents the action of creating place.
     */
    CREATE_PLACE,

    /**
     * Represents the action of deleting place.
     */
    DELETE_PLACE,

    /**
     * Represents the action of updating place.
     */
    UPDATE_PLACE, LOGOUT,
}
